<?php


class User
{
    private $id;
    private $email;
    private $password;
    private $name;
    private $surname;
    private $phone;
    private $avatar;
    private $joinTime;

    public function __construct(string $email, string $password, string $name, string $surname, string $phone,
                                string $avatar=null, string $joinTime=null, $id=null)
    {
        $this->id = $id;
        $this->email = $email;
        $this->password = $password;
        $this->name = $name;
        $this->surname = $surname;
        $this->phone = $phone;
        $this->avatar = $avatar;
        $this->joinTime = $joinTime;
    }

    public function getId()
    {
        return $this->id;
    }

    public function setId($id): void
    {
        $this->id = $id;
    }

    public function getEmail(): string
    {
        return $this->email;
    }

    public function setEmail(string $email): void
    {
        $this->email = $email;
    }

    public function getPassword(): string
    {
        return $this->password;
    }

    public function setPassword(string $password): void
    {
        $this->password = $password;
    }

    public function getName(): string
    {
        return $this->name;
    }

    public function setName(string $name): void
    {
        $this->name = $name;
    }

    public function getSurname(): string
    {
        return $this->surname;
    }

    public function setSurname(string $surname): void
    {
        $this->surname = $surname;
    }

    public function getPhone(): string
    {
        return $this->phone;
    }

    public function setPhone(string $phone): void
    {
        $this->phone = $phone;
    }

    public function getAvatar(): string
    {
        return $this->avatar;
    }

    public function setAvatar(string $avatar): void
    {
        $this->avatar = $avatar;
    }

    public function getJoinTime(): string
    {
        return $this->joinTime;
    }

    public function setJoinTime(string $joinTime): void
    {
        $this->joinTime = $joinTime;
    }
}